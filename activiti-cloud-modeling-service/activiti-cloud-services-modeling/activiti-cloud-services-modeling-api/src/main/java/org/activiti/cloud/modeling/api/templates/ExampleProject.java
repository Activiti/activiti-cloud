package org.activiti.cloud.modeling.api.templates;

import static com.fasterxml.jackson.annotation.JsonInclude.Include.NON_NULL;
import com.fasterxml.jackson.annotation.JsonIgnoreProperties;
import com.fasterxml.jackson.annotation.JsonInclude;

@JsonIgnoreProperties(ignoreUnknown = true)
@JsonInclude(NON_NULL)
public class ExampleProject {

    private String id;
    private ExampleProjectExtension extensions;

    public ExampleProject() {
    }

    public ExampleProject(String id) {
        this.id = id;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public ExampleProjectExtension getExtensions() {
        return extensions;
    }

    public void setExtensions(ExampleProjectExtension extensions) {
        this.extensions = extensions;
    }
}
